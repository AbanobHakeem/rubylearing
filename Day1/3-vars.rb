# Ruby Variables and Data Types

# 1. String
name = "Alice"
greeting = 'Hello'

# 2. Integer
age = 25
year = 2024

# 3. Float
height = 1.68
pi = 3.14159

# 4. Boolean
is_student = true
has_passed = false

# 5. Array
colors = ["red", "green", "blue"]

# 6. Hash
person = { name: "Bob", age: 30 }

# 7. Symbol
status = :active

# 8. Nil 
nothing = nil
#everone elase calling it null or undefined but in ruby it is nil HAHAHAHAH

# Demonstration
puts "String: #{name}, #{greeting}"
puts "Integer: #{age}, #{year}"
puts "Float: #{height}, #{pi}"
puts "Boolean: #{is_student}, #{has_passed}"
puts "Array: #{colors.inspect}"
puts "Hash: #{person.inspect}"
puts "Symbol: #{status}"
puts "Nil: #{nothing.inspect}"
