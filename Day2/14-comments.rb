# Ruby Lesson: Comments

# Comments are lines in your code that are ignored by the Ruby interpreter.
# They are used to explain what your code does, making it easier to understand.

# Single-line comments start with a hash symbol (#).
# Example:
# This is a single-line comment.

puts "Hello, world!" # This prints a message to the screen

=begin
Multi-line comments can be written using =begin and =end.
Everything between these lines is ignored by Ruby.
This is useful for longer explanations.
=end

puts "Comments make your code readable!"